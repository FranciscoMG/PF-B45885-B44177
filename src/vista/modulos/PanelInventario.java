    public void setJLabel_Agotados(ResultSet rsTabla) {
        String mensaje = "<HTML><FONT COLOR=\"red\">Productos Agotados:</FONT><BR>";
        try {
            while (rsTabla.next()) {
                if (rsTabla.getInt("cantidad") < 2) {
                    mensaje += rsTabla.getString("idProducto") + ", ";
                }
            }
        } catch (SQLException ex) {
        }
        mensaje = mensaje.substring(0, mensaje.length() - 2);
        mensaje += "</HTML>";
        this.jLabel_Agotados.setText(mensaje);
    }

    public void setjTable_Productos(ResultSet rsTabla) {
        DefaultTableModel tabla = new DefaultTableModel(new String[]{"Cod. producto", "Nombre producto", "Cod. proveedor", "Nombre proveedor", "Cantidad", "Ãšltima compra"}, 0);
        try {
            ResultSetMetaData columnas = rsTabla.getMetaData();
            while (rsTabla.next()) {
                Object[] fila = new Object[columnas.getColumnCount()];
                for (int i = 0; i < columnas.getColumnCount(); i++) {
                    fila[i] = rsTabla.getObject(i + 1);
                }
                tabla.addRow(fila);
            }
        } catch (SQLException ex) {
        }
        this.jTable_Productos.setModel(tabla);
        this.jScrollPane_Tabla.setViewportView(this.jTable_Productos);
    }
